export function getTranslation(lang = 'en', ...keys) {
  let current = translations[lang] || translations.en;
  for (const key of keys) {
    current = current?.[key];
    if (current == null) {
      return translations.en?.[keys[0]]?.[keys[1]] ?? null;
    }
  }
  return current;
}

